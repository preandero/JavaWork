package com.lec.java.while02;

public class While02Main {

	public static void main(String[] args) {
		System.out.println("while 연습");
		
		// 구구단 2단
		
		int two = 1;
		while (two < 10) {
			System.out.println("2 x " + two + " = " + (2 * two));
			two++;
		}

	} // end main()

} // end class While02Main
